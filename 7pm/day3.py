# logical operator

# and 

# True    and     True   ------> True
# False   and     True   ------> False
# True    and     False  ------> False
# False   and     False  ------> False

print(2 == 2  and  1 == 1)
print(3 == 1 and 4 == 4)
print(3 == 3 and 3 == 2)
print(1 == 0 and 4 == 3)

# or 
# True   or    True  -----> True 
# False  or    True  -----> True
# True   or    False -----> True
# False  or    False -----> False

print(2 == 2 or 3 == 3)
print(1 == 9 or 4 == 4)
print(7 == 7 or 8 == 3)
print(4 == 6 or 9 == 7)

# not
# not False ---> True 
# not True  ---> False

print(not 2 == 1)
